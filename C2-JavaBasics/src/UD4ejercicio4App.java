
public class UD4ejercicio4App {

	    public static void main(String[] args) {
	        // Declaramos la variable N y le asignamos un valor
	        int N = 1;

	        // Mostramos el valor inicial de N
	        System.out.println("Valor inicial de N = " + N);

	        // Incrementamos N en 77
	        N += 77;
	        System.out.println("N + 77 = " + N);

	        // Decrementamos N en 3
	        N -= 3;
	        System.out.println("N - 3 = " + N);

	        // Doblamos el valor de N
	        N *= 2;
	        System.out.println("N * 2 = " + N);
	    }
	}

